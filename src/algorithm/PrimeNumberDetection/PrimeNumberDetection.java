public class PrimeNumberDetection {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int n = in.nextInt();
        for (int i = 0; i < n; i++) {
            int k = 0;
            int num = in.nextInt();
            for (int j = 2; j < num; j++) {
                if(num % j == 0){
                    System.out.println("No");
                    break;
                }
                else {
                    k++;
                }
            }
            if(k == num - 2){
                System.out.println("Yes");
            }
        }
    }
}
