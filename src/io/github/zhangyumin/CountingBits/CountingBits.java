public class CountingBits {
    public static void main(String[] args) {
        int num = 5;
        ArrayList nums = new ArrayList();
        for(int i = 0; i <= num ; i++){
            int count = 0;
            int j = i;
            while(j != 0){
                if(j % 2 == 1){
                    count ++;
                }
                j = j / 2;
            }
            nums.add(count);
        }
        System.out.println(nums.toString());
    }
}
