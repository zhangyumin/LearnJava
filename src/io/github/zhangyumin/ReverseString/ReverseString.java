public class ReverseString {
    public static void main(String[] args) {
        String s = "hello";
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < s.length(); i++) {
            sb.append(s.charAt(s.length() - i - 1));
        }
        System.out.println(sb);
    }
}
